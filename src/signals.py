"""
Signal and information environment.

Signals are the mechanism by which ground truth reaches agents — imperfectly.
Each signal has:
  - precision: how informative (0 = noise, 1 = perfect)
  - cost: how expensive to produce (costly signals are more credible)
  - lag: turns of delay before delivery
  - bias: systematic distortion (disinformation)
  - reach: which agents receive it

Costly signaling theory: actions update beliefs more than words,
weighted by cost. A missile launch (expensive, irreversible) is a
high-precision signal. A tweet is cheap talk.
"""

